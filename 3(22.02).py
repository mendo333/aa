a = int(input("Введите первое число:\t"))
b = int(input("Введите второе число:\t"))

print(f"сумма {a} и {b} = {a+b}")
print(f"разность {a} и {b} = {a-b}")
print(f"деление {a} на {b} = {a/b}")
print(f"округление деления до 3 знака {a} на {b} = {round(a/b, 3)}")
print(f"округление деления {a} на {b} = {round(a/b)}")
print(f"умножение {a} на {b} = {a*b}")
print(f"деление без остатка {a} на {b} = {a//b}")
print(f"возведение в степень {a} в {b} = {a**b}")
print(f"остаток от деления {a} на {b} = {a%b}")